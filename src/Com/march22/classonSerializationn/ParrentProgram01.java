package Com.march22.classonSerializationn;

public class ParrentProgram01
{
    @SuppressWarnings("unused")
	public static void main(String[] args)
    {
    	int row = 5;
    	
    	for(int i = row; i >= 1 ;i++)
    	{
    		for(int j=1;i <= i;j++)
    		{
    			System.out.print("* ");
    		}
    		System.out.println();
    	}
		
	}
	
	
	
}
